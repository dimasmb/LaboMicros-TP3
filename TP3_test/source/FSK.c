FSK_t FSK_modulation(char* uart_str)
{
  static FSK_t mod;
  mod.start = 0;
  mod.stop = 1;
  mod.data = 0;
  mod.none = 0;

  uint8_t par_cont = 0;
  for(int i=0; i<FSK_DATA_SIZE ; i++)
  {
    if(uart_str[i] == '1')
    {
      mod.data |= (1<<(FSK_DATA_SIZE-1-i));
      par_cont++;
    }
    else
    {
      mod.data &= ~(1<<(FSK_DATA_SIZE-1-i));
    }
  }

  if(par_cont & (1<<0))
  {
    mod.parity = 0;
  }
  else mod.parity = 1;

  return mod;
}

