float firFilter(float x,int ord,float *h,float * buffer){
    int y=0;
    buffer[0]=x;
    
    int i=0;
    for (i=0;i<=ord;i++){
        y=y+(buffer[i]*h[i]);
    }
    int j=0;
    for (j=ord;j>=1;j--){
        buffer[j]=buffer[j-1];
    }


}

